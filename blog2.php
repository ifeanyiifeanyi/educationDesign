                          <p class="mb-15">The pastoral care of schools is part of the identity of Catholic schools. Salvation of soul is the supreme law (Canon 1752); hence pastoral care is a major concern in Onitsha Archdiocesan schools.</p>
                          <p class="mb-15">In view of the above, all Catholic schools in Onitsha Archdiocese have a functional priest-chaplain that takes care of the pastoral needs of both students and staff. This chaplain, usually, is different from the manager/principal of the school. This is desirable so as to separate the external and internal functions according to the mind of the Church. Pastoral care of the school requires that the Holy Mass be celebrated regularly and daily for the entire school community where possible. However, it is expected that Holy mass should be celebrated in all schools at least twice a term at the beginning and closing.</p>
